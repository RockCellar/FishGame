function Overlapcode (mySprite: Sprite) {
    if (mySprite.tileKindAt(TileDirection.Center, assets.tile`myTile0`)) {
        mySprite.setKind(SpriteKind.AAAAAA)
        mySprite.vy = 0
        for (let value of sprites.allOfKind(SpriteKind.CaughtFish)) {
            value.startEffect(effects.fire)
            value.follow(the_Cat)
            music.baDing.play()
            info.changeScoreBy(1)
            textSprite = textsprite.create("+1")
            textSprite.setVelocity(-21, -39)
            textSprite.setPosition(56, 43)
            textSprite.lifespan = 300
            pause(200)
            pause(200)
        }
        mySprite.destroy()
    }
}
